    public static NumberMaterial getNumberMaterial(ToolMaterial tm) {
        if (tm == null) {
            return new NumberMaterial(0);
        }
        for (Object o : ToolMain.jsonObject.values()) {
            if (!(o instanceof JSONObject)) continue;

            JSONObject j = (JSONObject) o;

            if (j.containsKey("id")) {
                int id = j.getInteger("id");
                int data = j.containsKey("data") ? j.getInteger("data") : 0;
                return new NumberMaterial(id,data);
            }

        }
        return new NumberMaterial(getNumberMaterial(ToolMaterial.valueOf(ToolMain.config.OPTIONS_UNKNOWNS_MATERIAL)));
    }

    public static ToolMaterial getAuto(String string) {
        if (NumberUtils.isNumber(string)) {
            return getMaterial(Integer.parseInt(string));
        } else if (string.contains(":")) {
            String[] strings = StringUtils.split(string,":");
            if (strings.length >= 2 && NumberUtils.isNumber(strings[0]) && NumberUtils.isNumber(strings[1])) {
                return getMaterial(Integer.parseInt(strings[0]),Integer.parseInt(strings[1]));
            }
        } else {
            return ToolMaterial.valueOf(Material.getMaterial(string).name());
        }
        return getUnknownMaterial();
    }

    public static ToolMaterial getMaterial(Material material) {
        return getMaterial(material.name());
    }

    public static ToolMaterial getMaterial(String material) {
        ToolMaterial tm = ToolMaterial.valueOf(material);
        return getUnknownMaterial();
    }

    public static ToolMaterial getMaterial(int id) {
        return getMaterial(new NumberMaterial(id));
    }

    public static ToolMaterial getMaterial(int id,int data) {
        return getMaterial(new NumberMaterial(id, data));
    }

    public static ToolMaterial getMaterial(NumberMaterial nm) {
        if (nm == null) {
            return getUnknownMaterial();
        }

        for (String s : ToolMain.jsonObject.keySet()) {
            JSONObject j = ToolMain.jsonObject.getJSONObject(s);

            if (j.containsKey("id")) {
                int id = j.getInteger("id");
                int data = j.containsKey("data") ? j.getInteger("data") : 0;

                if (nm.getId() == id && nm.getData() == data) {
                    return ToolMaterial.valueOf(s.toUpperCase());
                }
            }
        }

        return ToolMaterial.STONE;
    }

    public static ToolMaterial getUnknownMaterial() {
        String unknown = ToolMain.config.OPTIONS_UNKNOWNS_MATERIAL;
        if (EnumUtils.isValidEnum(ToolMaterial.class,unknown))
            return ToolMaterial.valueOf(unknown);
        else
            return ToolMaterial.STONE;
    }

